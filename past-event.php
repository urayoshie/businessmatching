$(document).on('click', 'a[href^="#"]', function (e) {
  const targetId = $(this).attr('href');

  // href="#" は無効なセレクターになるため除外
  if (targetId === '#' || !targetId || !/^#[\w-]+$/.test(targetId)) {
    return;
}

  const $target = $(targetId);

  // モーダル内にターゲット要素が存在する場合のみスクロール
  if ($target.length && $(this).closest('.modal').length) {
    e.preventDefault();
    const position = $target.offset().top - $(this).closest('.modal').offset().top + $(this).closest('.modal').scrollTop();

    $(this).closest('.modal').animate({ scrollTop: position }, 500);
  }
});
